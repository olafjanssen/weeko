    function renderDay(day) {
        var el = document.createElement('div');
        el.classList.add('calendar-day');

        el.setAttribute('data-weekday', day.datum.weekday());

        var content = '';

        content += '<h1 class="weekday-title">' + day.datum.locale('nl').format('dddd') + '</h1>';
        content += '<img src="emojis/' + weekdayImg[day.datum.weekday()] + '.png" class="weekday-image">';
        content += '<h2 class="monthday-title">' + day.datum.locale('nl').format('D MMMM') + '</h2>';

        content += '<div class="event-icons">';
        day.events.forEach(function (ev) {
            content += '<div class="event-icon">' + ev.icons + '</div>';
            if (ev.description) {
                content += '<div class="event-description">' + ev.description + '</div>'
            }
        });
        content += '</div>';

        content += '<div class="weather-temp">' + (day.maxTemp ? Math.round(day.maxTemp - 272.15) + '°C' : '') + '</div>';
        content += '<div class="weather-icons">';
        day.weatherIcons.forEach(function (wi) {
            content += '<div class="weather-icon" style="background-image:url(' + wi + ');"></div>';
        });
        content += '</div>';

        if (day.datum.diff(moment().startOf('day')) === 0) {
            el.classList.add('today');

            // get hour
            var semiHourOffset = moment().minutes() > 15 && moment().minutes() < 45 ? 12 : 0;
            var modHour = (moment().add(15, 'minutes').startOf('hour').hours()) % 12;
            var hourIndex = (modHour === 0 ? 12 : modHour) - 1 + semiHourOffset;
            console.log(semiHourOffset, hourIndex, timeImg[hourIndex]);
            content += '<div class="today-message"><img src="emojis/' + timeImg[hourIndex] + '.png"</div>';

            requestAnimationFrame(function () {
                element.parentElement.scrollLeft += el.getBoundingClientRect().left;
            });
        }

        el.innerHTML = content;
        element.appendChild(el);
    }

    function ucs2decode(string) {
        var output = [];
        var counter = 0;
        var length = string.length;
        while (counter < length) {
            var value = string.charCodeAt(counter++);
            if (value >= 0xD800 && value <= 0xDBFF && counter < length) {
                // It's a high surrogate, and there is a next character.
                var extra = string.charCodeAt(counter++);
                if ((extra & 0xFC00) == 0xDC00) { // Low surrogate.
                    output.push(((value & 0x3FF) << 10) + (extra & 0x3FF) + 0x10000);
                } else {
                    // It's an unmatched surrogate; only append this code unit, in case the
                    // next code unit is the high surrogate of a surrogate pair.
                    output.push(value);
                    counter--;
                }
            } else {
                output.push(value);
            }
        }
        return output;
    }


    function translateSummary(summary) {
        var output = '';

        if (summary.substr(0, 4) === 'http') {
            output += '<img src="' + summary + '">';
        } else {
            var characters = ucs2decode(summary);
            characters.forEach(function (c) {
                if (emojiKeys[c.toString(16)]) {
                    output += '<img src="emojis/' + c.toString(16) + '.png">';
                } else {
                    output += String.fromCodePoint(c);
                }
            });
        }
        return output;
    }

    function compileDayData(datum) {
        return new Promise(function (resolve, reject) {
            var day = {};
            day.datum = datum;
            day.maxTemp = 0;

            // get weather
            day.weatherIcons = weather.list.filter(function (w) {
                return moment(w.dt * 1000).startOf('day').diff(day.datum) === 0;
            }).map(function (a) {
                day.maxTemp = window.Math.max(day.maxTemp, a.main.temp_max);
                return 'http://openweathermap.org/img/w/' + a.weather[0].icon + '.png';
            });

            // get events
            day.events = [];

            events.filter(function (e) {
                var start = moment(e.start.date ? e.start.date : e.start.dateTime).startOf('day'),
                    end = e.end.date ? moment(e.end.date).subtract(1, 'day') : moment(e.end.dateTime);

                return datum.diff(start) >= 0 && datum.diff(end) <= 0;
            }).forEach(function (e) {
                day.events.push({icons: translateSummary(e.description), description: e.summary});
            });

            resolve(day);
        });
    }

    function loadJSON(path) {
        return new Promise(function (resolve, reject) {
            var fromStorage = false, weatherData;
            try {
                fromStorage = localStorage.getItem('weather') && moment.duration(moment(JSON.parse(localStorage.getItem('weather')).dateTime).diff(moment())).hours() < 3
                weatherData = JSON.parse(JSON.parse(localStorage.getItem('weather')).rawData);
            } catch (e) {
                fromStorage = false;
            }

            if (fromStorage) {
                resolve(weatherData);
            } else {
                var xhr = new XMLHttpRequest();
                xhr.onreadystatechange = function () {
                    if (xhr.readyState === XMLHttpRequest.DONE) {
                        if (xhr.status === 200) {
                            localStorage.setItem('weather', JSON.stringify({
                                dateTime: new Date(),
                                rawData: xhr.responseText
                            }));
                            resolve(JSON.parse(xhr.responseText));
                        } else {
                            reject(xhr);
                        }
                    }
                };
                xhr.open("GET", path, true);
                xhr.send();
            }
        });
    }

